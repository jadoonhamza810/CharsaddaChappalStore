package Model;

public class Cart {
    private String pid,pname,date,time,discount,price,stock,quantity,image,sellerID;

    public Cart(){

    }

    public Cart(String pid, String pname, String date, String time, String discount, String price, String stock, String quantity, String image, String sellerID)
    {
        this.pid = pid;
        this.pname = pname;
        this.date = date;
        this.time = time;
        this.discount = discount;
        this.price = price;
        this.stock = stock;
        this.quantity = quantity;
        this.image = image;
        this.sellerID = sellerID;
    }

    public String getPid() {
        return pid;
    }

    public void setPid(String pid) {
        this.pid = pid;
    }

    public String getPname() {
        return pname;
    }

    public void setPname(String pname) {
        this.pname = pname;
    }

    public String getDate() {
        return date;
    }

    public void setDate(String date) {
        this.date = date;
    }

    public String getTime() {
        return time;
    }

    public void setTime(String time) {
        this.time = time;
    }

    public String getDiscount() {
        return discount;
    }

    public void setDiscount(String discount) {
        this.discount = discount;
    }

    public String getPrice() {
        return price;
    }

    public void setPrice(String price) {
        this.price = price;
    }

    public String getStock() {
        return stock;
    }

    public void setStock(String stock) {
        this.stock = stock;
    }

    public String getQuantity() {
        return quantity;
    }

    public void setQuantity(String quantity) {
        this.quantity = quantity;
    }

    public String getImage() {
        return image;
    }

    public void setImage(String image) {
        this.image = image;
    }

    public String getSellerID() {
        return sellerID;
    }

    public void setSellerID(String sellerID) {
        this.sellerID = sellerID;
    }
}
